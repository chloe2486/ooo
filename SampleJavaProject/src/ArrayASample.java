
public class ArrayASample {

	public static void main(String[] args) {
		int[] score;
		score = new int[3];
		
		score[0] = 60;
		score[1] = 70;
		score[2] = 80;
		System.out.println("一人目の点数：" + score[0]);
		System.out.println("二人目の点数：" + score[1]);
		System.out.println("三人目の点数：" + score[2]);
		
	}

}
